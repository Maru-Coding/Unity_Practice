using UnityEngine;

public class OnMouse : MonoBehaviour
{
     private void OnMouseEnter()
     {
          print("마우스 입력됨");
     }
     private void OnMouseExit()
     {
          print("마우스 입력됨");
     }
     private void OnMouseDown()
     {
          print("마우스 누름");
     }
     private void OnMouseUp()
     {
          print("마우스 뗌");
     }
}
